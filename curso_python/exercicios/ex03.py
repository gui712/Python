# Qual é maior

primeiro_valor = input('Digite o primeiro valor: ')
segundo_valor = input('Digite o segundo valor: ')

if primeiro_valor >= segundo_valor:
    print(f'O primeiro valor digitado {primeiro_valor}, é maior ou igual que o segundo valor digitado {segundo_valor}')
else:
    print(f'O segundo valor digitado {segundo_valor}, é maior que o primeiro valor digitado {primeiro_valor}')
