# calcular imc
# Luiz Otavio altura 1.80 peso 95 imc = 29.32098..
# nome tem 1.80 de altura pesa 95 e seu IMC é
# IMC = PESO / (altura x altura)

nome = 'Luiz Otavio'
altura = 1.80
peso =  95

imc = peso / (altura * altura)

print(nome, 'tem', altura, 'de altura, pesa', peso,'quilos e seu IMC É', imc)