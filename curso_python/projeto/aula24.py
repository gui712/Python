variavel ='Olá mundo'

print(variavel[0:5])
print(variavel[:5])
print(variavel[3])
print(variavel[-8:-2])
print(variavel[0:9:2])# passo contando de 2 em 2
print(variavel[-1:-10:-1])# ivertido
print(len(variavel)) #contagem de caracteres  diferente de indices 