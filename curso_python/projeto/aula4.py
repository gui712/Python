#Tipos int e float
# int > Número inteiro
# o tipo int representa qualquer número
#positivo ou negativo, int sem sinal é positivo
print(11)
print(0)
print(-11)

# float > Número com ponto flutuante
# o tipo float representa qualquer número
#positivo ou negativo com ponto flutuante, float sem sinal é positivo
print(11.1)
print(1.1, 0.0, -1.5)
# a função type() mostra o tipo
print(type('Gui'))
print(type(1))
print(type(1.8))