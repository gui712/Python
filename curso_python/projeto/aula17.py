"""
Operadpres de comparação(relacionais)

OP          SIGNIFICADO         Exemplo(True)
>           Maior               2 > 1
>=          Maior ou Igual      2 >= 2
<           Menor               1 < 2
<=          Menor ou igual      2 <= 2      
==          Igual               'a' == 'a'
!=          Diferente           'a' != 'b'
sempre retorna um boolean True or False
"""

maior = 5 > 2
maior_ou_igual = 3 >= 3 
menor = 4 < 8
menor_ou_igual = 2 <= 5
igual = 'a' == 'a'
diferente = 'c' != 'e'
print(maior)
print(maior_ou_igual)
print(menor)
print(menor_ou_igual)
print(igual)
print(diferente)